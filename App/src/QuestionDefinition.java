public class QuestionDefinition  {
    String definition;
    String reponse;
    public QuestionDefinition(String definition,String reponse) {
        this.definition=definition;
        this.reponse=reponse;
    }
    public String getDefinition() {
        return definition;
    }
    public String getReponse() {
        return reponse;
    }
    
}
